# Explicação sobre Operadores Aritméticos e Booleanos

# Operadores Aritméticos:

# Os operadores aritméticos são utilizados em expressões matemáticas para realizar operações como adição, subtração, multiplicação, divisão, entre outras. Aqui estão os principais:

# 1. Adição (+): Utilizado para somar dois valores.
# Exemplo:
soma = 5 + 3  # Resultado será 8

# 2. Subtração (-): Utilizado para subtrair um valor de outro.
# Exemplo:
subtracao = 10 - 4  # Resultado será 6

# 3. Multiplicação (*): Utilizado para multiplicar dois valores.
# Exemplo:
multiplicacao = 2 * 6  # Resultado será 12

# 4. Divisão (/): Utilizado para dividir um valor pelo outro.
# Exemplo:
divisao = 15 / 3  # Resultado será 5.0 (divisão de ponto flutuante)

# 5. Divisão inteira (//): Realiza uma divisão, retornando apenas a parte inteira do resultado.
# Exemplo:
divisao_inteira = 17 // 5  # Resultado será 3 (parte inteira de 17/5)

# 6. Módulo (%): Retorna o resto da divisão entre dois números.
# Exemplo:
resto_divisao = 17 % 5  # Resultado será 2 (resto da divisão de 17 por 5)


# Operadores Booleanos:

# Os operadores booleanos são utilizados para realizar operações lógicas entre valores booleanos (Verdadeiro ou Falso). Aqui estão os principais:

# 1. E (and): Retorna verdadeiro se ambas as expressões forem verdadeiras.
# Exemplo:
expressao_and = (5 > 3) and (10 < 20)  # Resultado será True

# 2. OU (or): Retorna verdadeiro se pelo menos uma das expressões for verdadeira.
# Exemplo:
expressao_or = (5 < 3) or (10 < 20)  # Resultado será True

# 3. NÃO (not): Inverte o valor de uma expressão booleana.
# Exemplo:
expressao_not = not (5 == 5)  # Resultado será False
